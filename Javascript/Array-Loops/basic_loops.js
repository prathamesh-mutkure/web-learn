const nums = new Array("ONE", "Two", "Three", "Four", "Five");

// FOR LOOP

for (let i = 0; i < nums.length; i++) {
  console.log(nums[i]);
}

// WHILE

let i = 0;

while (i < nums.length) {
  console.log(nums[i]);
  i++;
}

// DO WHILE

let j = 0;

do {
  console.log(nums[j]);
  j++;
} while (j < nums.length);
